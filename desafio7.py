# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

valor_do_lado_do_quadrado = int(input('digite o valor do lado do quadrado: '))
area_do_quadrado = valor_do_lado_do_quadrado ** 2
dobro_da_area = area_do_quadrado * 2
print('o valor da area do quadrado é', dobro_da_area)